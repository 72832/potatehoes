//auton
void auton(){
	startTask(straighten);

	//1200 from place to flag or to alliance park
	//2000 from place to center
	punch();

	//intake on
	startTask(intakeOn);

	//drive forward to toggle small flag
	if(SensorValue[rightEncoder]<=1500){
		leftAuton(-127);
		rightAuton(127);
	}

	if(vexRT[Btn8L]==1){
		stopTask(autonomous);
	}


	delayFunc(500);

	//intake stop
	stopTask(intakeOn);
	startTask(intakeOff);
	stopTask(intakeOff);

	stopTask(straighten);

}

